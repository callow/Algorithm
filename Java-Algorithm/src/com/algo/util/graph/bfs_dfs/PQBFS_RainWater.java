package com.algo.util.graph.bfs_dfs;

import java.util.PriorityQueue;

/**
 *  二维接雨水
  给你一个 m * n 的矩阵，其中的值均为非负整数，代表二维高度图每个单元的高度
  请计算图中形状最多能接多少体积的雨水。
  测试链接 : https://leetcode.cn/problems/trapping-rain-water-ii/
 */
public class PQBFS_RainWater {

	public static int trapRainWater(int[][] height) {
		int[] move = {-1,0,1,0,-1};
		int n = height.length;
		int m = height[0].length;
		
		// 0: 行， 1: 列， 2: 水线 ： 根据水线高度，谁小谁先弹出（小根堆）
		PriorityQueue<int[]> heap = new PriorityQueue<int[]>((a,b) -> a[2] - b[2]);
		boolean[][] visited= new boolean[n][m];
		
		for (int i = 0; i < n; i++) {
			for (int j = 0; j < m; j++) {
				if (i == 0 || i == n - 1 || j == 0 || j == m - 1) { // 边界的格子进pq
					// 边界
					heap.add(new int[] { i, j, height[i][j] });
					visited[i][j] = true;
				} else {
					visited[i][j] = false;
				}
			}
		}
		
		int ans = 0;
		
		while (!heap.isEmpty()) {
			int[] record = heap.poll(); // 　每次弹出最薄弱点 - pq
			int r = record[0];
			int c = record[1];
			int w = record[2];
			ans += w - height[r][c]; // 结算水位
			for (int i = 0, nr, nc; i < 4; i++) { // 上下左右四个方向开始BFS
				nr = r + move[i];
				nc = c + move[i + 1];
				if (nr >= 0 && nr < n && nc >= 0 && nc < m && !visited[nr][nc]) { // 不能越界，不可以重复
					heap.add(new int[] { nr, nc, Math.max(height[nr][nc], w) }); // 触发我的水线和我自己 Max
					visited[nr][nc] = true;
				}
			}
		}
		return ans;
	}
}
